package Escape;

public class EscapeSequencesDemo {
    public static void main(String[] args) {
        // Example of backslash
        String backslash = "This is a backslash: \\";
        System.out.println(backslash);

        // Example of double quote
        String doubleQuote = "She said, \"Hello!\"";
        System.out.println(doubleQuote);

        // Example of single quote
        String singleQuote = "It\'s a sunny day!";
        System.out.println(singleQuote);

        // Example of new line
        String newLine = "First Line\nSecond Line";
        System.out.println(newLine);

        // Example of tab
        String tab = "Name:\tJohn Doe";
        System.out.println(tab);

        // Example of Unicode character
        String unicode = "Unicode: \u00A9";
        System.out.println(unicode);
    }
}
