/*
 Create a program to calculate the Area of a Triangle.
    Area of triangle = 1/2*B*H
 */
package PracticeOne;

public class Area {
    public static void main(String[] args) {
        double base = 5.0;
        double height = 10.0;
        double Area = 0.5 * (base * height);

        System.out.println("Area : " + Area);
    }
}
