package Operators;

public class Arithmetic {
    public static void main(String[] args) {

        System.out.println("+" + " : " + "use for Addition");
        System.out.println(); // use for new line

        System.out.println("-" + " : " + "use for Substraction");
        System.out.println(); // use for new line

        System.out.println("*" + " : " + "use for Multiplication");
        System.out.println(); // use for new line

        System.out.println("/" + " : " + "use for Division");
        System.out.println(); // use for new line

        System.out.println("%" + " : " + "use for Remainder");
    }
}
